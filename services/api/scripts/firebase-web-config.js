/**
 * Provisions a Firebase WEB app for the admin dashboard from the backend service
 * account (no `firebase login`). Creates the web app if missing, fetches its
 * config, and writes apps/admin_dashboard/.env.local.
 *
 * Run from services/api:  node scripts/firebase-web-config.js
 */
try { require('dotenv').config(); } catch {}
const fs = require('fs');
const path = require('path');
const jwt = require('jsonwebtoken');

const PROJECT = process.env.FIREBASE_PROJECT_ID || 'kawkaw08';
const CLIENT_EMAIL = process.env.FIREBASE_CLIENT_EMAIL;
const PRIVATE_KEY = (process.env.FIREBASE_PRIVATE_KEY || '').replace(/\\n/g, '\n');
const API_BASE_URL = process.env.ADMIN_API_BASE_URL || 'http://localhost:3000/api/v1';
const TEST_MODE = process.env.ADMIN_TEST_MODE || 'false';

const envPath = path.resolve(process.cwd(), '..', '..', 'apps', 'admin_dashboard', '.env.local');

async function token() {
  const now = Math.floor(Date.now() / 1000);
  const assertion = jwt.sign(
    {
      iss: CLIENT_EMAIL,
      scope: 'https://www.googleapis.com/auth/cloud-platform https://www.googleapis.com/auth/firebase',
      aud: 'https://oauth2.googleapis.com/token',
      iat: now,
      exp: now + 3600,
    },
    PRIVATE_KEY,
    { algorithm: 'RS256' },
  );
  const res = await fetch('https://oauth2.googleapis.com/token', {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body: new URLSearchParams({ grant_type: 'urn:ietf:params:oauth:grant-type:jwt-bearer', assertion }),
  });
  const json = await res.json();
  if (!res.ok) throw new Error(`token exchange failed (${res.status}): ${JSON.stringify(json)}`);
  return json.access_token;
}

async function api(tok, method, url, body) {
  const res = await fetch(url, {
    method,
    headers: { Authorization: `Bearer ${tok}`, 'Content-Type': 'application/json' },
    body: body ? JSON.stringify(body) : undefined,
  });
  const json = await res.json().catch(() => ({}));
  if (!res.ok) throw new Error(`${method} ${url} -> ${res.status}: ${JSON.stringify(json)}`);
  return json;
}

async function main() {
  if (!CLIENT_EMAIL || !PRIVATE_KEY) throw new Error('FIREBASE_CLIENT_EMAIL/PRIVATE_KEY missing in .env');
  const tok = await token();
  const base = `https://firebase.googleapis.com/v1beta1/projects/${PROJECT}`;

  console.log('Listing web apps...');
  const list = await api(tok, 'GET', `${base}/webApps`);
  let app = (list.apps || []).find((a) => a.displayName === 'Kaw Kaw Admin') || (list.apps || [])[0];

  if (!app) {
    console.log('No web app — creating "Kaw Kaw Admin"...');
    const op = await api(tok, 'POST', `${base}/webApps`, { displayName: 'Kaw Kaw Admin' });
    let done = op.done ? op : null;
    for (let i = 0; i < 20 && !done; i++) {
      await new Promise((r) => setTimeout(r, 3000));
      const o = await api(tok, 'GET', `https://firebase.googleapis.com/v1beta1/${op.name}`);
      if (o.done) done = o;
    }
    if (!done) throw new Error('Web app creation timed out');
    const appId = done.response && done.response.appId;
    const relist = await api(tok, 'GET', `${base}/webApps`);
    app = (relist.apps || []).find((a) => a.appId === appId);
  }
  if (!app) throw new Error('Could not resolve the web app');
  console.log(`Web app: ${app.appId}`);

  const cfg = await api(tok, 'GET', `${base}/webApps/${app.appId}/config`);

  const lines = [
    '# Generated by services/api/scripts/firebase-web-config.js',
    `NEXT_PUBLIC_API_BASE_URL=${API_BASE_URL}`,
    `NEXT_PUBLIC_FIREBASE_API_KEY=${cfg.apiKey}`,
    `NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN=${cfg.authDomain || `${PROJECT}.firebaseapp.com`}`,
    `NEXT_PUBLIC_FIREBASE_PROJECT_ID=${cfg.projectId || PROJECT}`,
    `NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET=${cfg.storageBucket || ''}`,
    `NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID=${cfg.messagingSenderId || ''}`,
    `NEXT_PUBLIC_FIREBASE_APP_ID=${cfg.appId}`,
    `NEXT_PUBLIC_FIREBASE_TEST_MODE=${TEST_MODE}`,
    '',
  ].join('\n');

  fs.writeFileSync(envPath, lines);
  console.log(`Wrote ${envPath}`);
  console.log('\n✓ Admin dashboard Firebase web config ready.');
  console.log('  Add this web app domain (localhost / admin.kawkaw.in) under Auth → Settings → Authorized domains if needed.');
}

main().catch((e) => {
  console.error(`\n✗ ${e.message}`);
  process.exit(1);
});
