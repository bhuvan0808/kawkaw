/**
 * Generates the customer app's Firebase config from the backend service account
 * (no `firebase login` needed) — IF the service account has Firebase management
 * permission. Writes android/app/google-services.json + lib/firebase_options.dart.
 *
 * Run from services/api:  node scripts/firebase-autoconfig.js
 */
try { require('dotenv').config(); } catch {}
const fs = require('fs');
const path = require('path');
const jwt = require('jsonwebtoken');

const PROJECT = process.env.FIREBASE_PROJECT_ID || 'kawkaw08';
const CLIENT_EMAIL = process.env.FIREBASE_CLIENT_EMAIL;
const PRIVATE_KEY = (process.env.FIREBASE_PRIVATE_KEY || '').replace(/\\n/g, '\n');
const PACKAGE = process.env.ANDROID_PACKAGE || 'in.kawkaw.kawkaw_customer';

// Which app to configure. Defaults preserve the original customer-app behaviour.
const APP_DIR = process.env.FIREBASE_APP_DIR || 'customer_app';
const DISPLAY_NAME = process.env.FIREBASE_DISPLAY_NAME || 'Kaw Kaw Customer';

const appDir = path.isAbsolute(APP_DIR)
  ? APP_DIR
  : path.resolve(process.cwd(), '..', '..', 'apps', APP_DIR);

async function token() {
  const now = Math.floor(Date.now() / 1000);
  const assertion = jwt.sign(
    {
      iss: CLIENT_EMAIL,
      scope: 'https://www.googleapis.com/auth/cloud-platform https://www.googleapis.com/auth/firebase',
      aud: 'https://oauth2.googleapis.com/token',
      iat: now,
      exp: now + 3600,
    },
    PRIVATE_KEY,
    { algorithm: 'RS256' },
  );
  const res = await fetch('https://oauth2.googleapis.com/token', {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body: new URLSearchParams({
      grant_type: 'urn:ietf:params:oauth:grant-type:jwt-bearer',
      assertion,
    }),
  });
  const json = await res.json();
  if (!res.ok) throw new Error(`token exchange failed (${res.status}): ${JSON.stringify(json)}`);
  return json.access_token;
}

async function api(tok, method, url, body) {
  const res = await fetch(url, {
    method,
    headers: { Authorization: `Bearer ${tok}`, 'Content-Type': 'application/json' },
    body: body ? JSON.stringify(body) : undefined,
  });
  const json = await res.json().catch(() => ({}));
  if (!res.ok) throw new Error(`${method} ${url} -> ${res.status}: ${JSON.stringify(json)}`);
  return json;
}

async function main() {
  if (!CLIENT_EMAIL || !PRIVATE_KEY) throw new Error('FIREBASE_CLIENT_EMAIL/PRIVATE_KEY missing in .env');
  console.log(`Authenticating as ${CLIENT_EMAIL} for project ${PROJECT}...`);
  const tok = await token();

  const base = `https://firebase.googleapis.com/v1beta1/projects/${PROJECT}`;
  console.log('Listing Android apps...');
  const list = await api(tok, 'GET', `${base}/androidApps`);
  let app = (list.apps || []).find((a) => a.packageName === PACKAGE);

  if (!app) {
    console.log(`No app for ${PACKAGE} — creating...`);
    const op = await api(tok, 'POST', `${base}/androidApps`, {
      displayName: DISPLAY_NAME,
      packageName: PACKAGE,
    });
    // Poll the long-running operation.
    let done = op.done ? op : null;
    for (let i = 0; i < 20 && !done; i++) {
      await new Promise((r) => setTimeout(r, 3000));
      const o = await api(tok, 'GET', `https://firebase.googleapis.com/v1beta1/${op.name}`);
      if (o.done) done = o;
    }
    if (!done) throw new Error('App creation timed out');
    const appId = done.response && done.response.appId;
    const relist = await api(tok, 'GET', `${base}/androidApps`);
    app = (relist.apps || []).find((a) => a.appId === appId || a.packageName === PACKAGE);
  }
  if (!app) throw new Error('Could not resolve the Android app.');
  console.log(`Android app: ${app.appId}`);

  console.log('Fetching config (google-services.json)...');
  const cfg = await api(tok, 'GET', `${base}/androidApps/${app.appId}/config`);
  const gsjson = Buffer.from(cfg.configFileContents, 'base64').toString('utf8');
  const gsPath = path.join(appDir, 'android', 'app', 'google-services.json');
  fs.writeFileSync(gsPath, gsjson);
  console.log(`Wrote ${gsPath}`);

  // Build firebase_options.dart from the google-services.json values.
  const gs = JSON.parse(gsjson);
  const projNum = gs.project_info.project_number;
  const projId = gs.project_info.project_id;
  const bucket = gs.project_info.storage_bucket || `${projId}.appspot.com`;
  const client = gs.client[0];
  const appId = client.client_info.mobilesdk_app_id;
  const apiKey = client.api_key[0].current_key;

  const dart = `// GENERATED by services/api/scripts/firebase-autoconfig.js — do not edit by hand.
import 'package:firebase_core/firebase_core.dart';
import 'package:flutter/foundation.dart' show defaultTargetPlatform, kIsWeb, TargetPlatform;

class DefaultFirebaseOptions {
  static FirebaseOptions get currentPlatform {
    if (kIsWeb) {
      throw UnsupportedError('Web is not a target for the customer app.');
    }
    switch (defaultTargetPlatform) {
      case TargetPlatform.android:
        return android;
      default:
        throw UnsupportedError('Only Android is configured. Run flutterfire configure for others.');
    }
  }

  static const FirebaseOptions android = FirebaseOptions(
    apiKey: '${apiKey}',
    appId: '${appId}',
    messagingSenderId: '${projNum}',
    projectId: '${projId}',
    storageBucket: '${bucket}',
  );
}
`;
  const dartPath = path.join(appDir, 'lib', 'firebase_options.dart');
  fs.writeFileSync(dartPath, dart);
  console.log(`Wrote ${dartPath}`);
  console.log('\n✓ Firebase config generated. Next: enable Phone auth + add SHA-1 in the console.');
}

main().catch((e) => {
  console.error(`\n✗ Auto-config failed: ${e.message}`);
  console.error('Fall back to: firebase login  then  flutterfire configure --project=kawkaw08');
  process.exit(1);
});
